    <h1>echo</h1>
    <h2>Description</h2>
    <p>La fonction echo en PHP permet d'afficher une ou plusieurs chaînes de caractères ou des variables à l'écran. Elle n'est pas vraiment considérée comme une fonction, mais plutôt comme une instruction d'affichage. Elle peut être utilisée pour afficher des informations sur la page web générée par le script PHP. Elle prend un ou plusieurs arguments séparés par des virgules, qui peuvent être des chaînes de caractères ou des variables. Elle ne renvoie rien.</p>
    <h2>Exemple</h2>
    <p>$name = "John";<br>
echo "Hello, ", $name, "!";
</p>

    <?php
$name = "John";
echo "Hello, ", $name, "!";

