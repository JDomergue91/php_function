<h1>strtoupper()</h1>
    <h2>Description</h2>
    <p>La fonction `strtoupper()` en PHP permet de convertir une chaîne de caractères en majuscules. Elle prend en paramètre la chaîne à convertir. Elle renvoie la chaîne convertie en majuscules.</p>
    <h2>Exemple</h2>
    <p>Voici un exemple d'utilisation de la fonction `strtoupper()` :</p>
    <p>
    <?php
    $originalString = "Bonjour tout le monde";
    $upperString = strtoupper($originalString);
    echo $upperString;
    ?>
    </p>
    <p>Dans cet exemple, on utilise la fonction `strtoupper()` pour convertir la chaîne $originalString en majuscules, puis on utilise la fonction `echo` pour afficher la chaîne convertie.
    </p>
